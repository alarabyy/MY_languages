package OOP.object;

public class Cars {
    private int id;
    private String name;
    float num;
    double phones;


    //////////////////////////////////////////
    public void setid(int id){
        this.id = id;
    }
    public int getid(){
        return id;
    }
    //////////////////////////////////////////
    public void setname(String name){
        name = name;
    }
    public String getname(){
        return name;
    }
    //////////////////////////////////////////
    public void setnum(float num){
        num = num;
    }
    public float getnum(){
        return num;
    }
    //////////////////////////////////////////
    public void setphone(double phones){
        phones = phones;
    }
    public double getphones(){
        return phones;
    }
    //////////////////////////////////////////

}
