class Employee {
    private String name;
    private int id;
  
    public Employee(String name, int id) {
      this.name = name; // 'this' refers to the current object
      this.id = id;
    }
  
    public void setName(String name) {
      this.name = name; // 'this' is used to differentiate between the parameter and the instance variable
    }
  
    public String getName() {
      return this.name; // 'this' is optional when accessing a non-static member from within the same class
    }
  }
  ////////////////////////////////////////////////////////////////////////
  class Manager extends Employee {
    private String department;
  
    public Manager(String name, int id, String department) {
      super(name, id); // Call parent class constructor using 'super'
      this.department = department;
    }
  
    @Override
    public void setName(String name) {
      super.setName(name); // Call parent class method using 'super'
      System.out.println("Also updating manager display name."); // Additional processing specific to Manager
    }
  }
